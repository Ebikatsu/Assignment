#問題1:統計テスト1の得点について統計が好きか嫌いかによって2群に分けたとき2群の平均値に有意な差はあるか
統計好き<-c(6,10,6,10,11,6,11,7)
統計嫌い<-c(5,3,5,9,4,4,9,5,8,7,7,9)
#分散が等質であるか検定
var.test(統計好き,統計嫌い)
#p値0.8124、有意水準5%で帰無仮説は棄却されない。よって分散は等質である。
t.test(統計好き,統計嫌い,var.equal=TRUE)
#p値0.05203、有意水準5%で帰無仮説は棄却されない。よって平均値に有意な差はない。
