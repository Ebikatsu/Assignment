#問題1:100回の対戦のうちAの勝利数が58、Bの勝利数が42の時、AはBより強いといえるか。またこの結果になる確率
#帰無仮説:AとBの強さは等しい。
#対立仮説:AはBよりも強い。
#検定統計量はAの勝利数
#有意水準は5%
#検定統計量の実現値はAの勝利数58勝
勝利数<-0:100
round(pbinom(勝利数,100,0.5),4)
#Aの勝利数58勝以上で棄却域に入るので帰無仮説を棄却する。よってAはBより強い。
#問題2:150回の対戦のうちAの勝利数が63、Bの勝利数が87の時、AとBの強さは等しくないといえるか。
#帰無仮説:AとBの強さは等しい。
#対立仮説:AとBの強さは等しくない。
#検定統計量はAの勝利数
#有意水準は5%
#検定統計量の実現値はAの勝利数63勝
勝利数<-0:150
round(pbinom(勝利数,150,0.5),4)
#p値は0.300で棄却域には入らないので、帰無仮説は棄却されない。よってAとBの強さは等しいとは言えない。
#問題3,4,5で前々回のshidouhou.txtを基に計算
指導法データ<-read.table("shidouhou.txt",header=TRUE)
#問題3:数学が嫌いな人は好きな人よりおおいかどうか二項判定によって判定
#帰無仮説:数学が嫌いな人と好きな人の人数に有意な差はない。
#対立仮説:数学が嫌いな人と好きな人の人数に有意な差がある。
#検定統計量は数学が嫌いな人の人数
#有意水準は5%
nrow(指導法データ[指導法データ$数学=="嫌い",])
#検定統計量の実現値は数学が嫌いな人の人数14人
数学が嫌いな人の人数の配列<-0:nrow(指導法データ)
round(pbinom(数学が嫌いな人の人数の配列,nrow(指導法データ),0.5),4)
#p値0.9793で棄却域に入るので、帰無仮説は棄却される。よって数学が嫌いな人と好きな人の人数に有意な差がある。
#問題4:数学が嫌いな人は統計も嫌いな人が多いかどうか二項判定によって判定
#帰無仮説:数学が嫌いな人の中で統計が嫌いな人と好きな人の人数に有意な差はない。
#対立仮説:数学が嫌いな人の中で統計が嫌いな人と好きな人の人数に有意な差がある。
#検定統計量は数学が嫌いな人のなかで統計が嫌いな人の人数
#有意水準は5%
nrow(指導法データ[(指導法データ$数学=="嫌い")&(指導法データ$統計=="嫌い"),])
#検定統計量の実現値は数学が嫌いな人の人数10人
数学が嫌いな人の中で統計が嫌いな人の人数の配列<-0:nrow(指導法データ[指導法データ$数学=="嫌い",])
round(pbinom(数学が嫌いな人の中で統計が嫌いな人の人数の配列,nrow(指導法データ[指導法データ$数学=="嫌い",]),0.5),4)
#p値0.9713で棄却域に入らないので、帰無仮説は棄却されない。よって数学が嫌いな人の中で統計が嫌いな人と好きな人の人数に有意な差があるとは言えない。
#問題5:心理学テストの点数が平均点以上な人は数学が嫌いな人が多いかどうか二項判定によって判定
#帰無仮説:心理学テストの点数が平均点以上な人の中で数学が嫌いな人と好きな人の人数に有意な差はない。
#対立仮説:心理学テストの点数が平均点以上な人の中で数学が嫌いな人と好きな人の人数に有意な差がある。
#検定統計量は心理学テストの点数が平均点以上な人の中で数学が嫌いな人の人数
#有意水準は5%
mean(指導法データ$心理学テスト)
nrow(指導法データ[(指導法データ$心理学テスト>10)&(指導法データ$数学=="嫌い"),])
