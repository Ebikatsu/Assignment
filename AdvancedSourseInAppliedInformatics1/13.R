round(pbinom(心理学テストの点数が平均点以上な人の中で数学が嫌いな人の人数の配列,nrow(指導法データ[指導法データ$心理学テスト>10,]),0.5),4)
#p値0.9102で棄却域に入らないので、帰無仮説は棄却されない。よって心理学テストの点数が平均点以上な人の中で数学が嫌いな人と好きな人の人数に有意な差があるとは言えない。
