#全ての問題でshidouhou.txtを読み込んで作成したデータフレームを基に計算
指導法データ<-read.table("shidouhou.txt",header=TRUE)
指導法データ
#問題1：統計テスト1と統計テスト2の相関係数を計算
cor(指導法データ$統計テスト1,指導法データ$統計テスト2)
#統計テスト1と統計テスト2の間には強い相関関係がある。
#問題2:性別が男であるデータの統計テスト1と統計テスト2の相関係数を計算
条件男<-指導法データ$性別=="男"
男データ<-指導法データ[条件男,]
cor(男データ$統計テスト1,男データ$統計テスト2)
#統計テスト1と統計テスト2の間には強い相関関係がある。
#問題3:数学嫌いで統計が嫌いであるデータの統計テスト1の統計2の相関係数を計算
問題3データ<-指導法データ[指導法データ$数学=="嫌い"&指導法データ$統計=="嫌い",]
cor(問題3データ$統計テスト1,問題3データ$統計テスト2)
#統計テスト1と統計テスト2の間には中程度の相関関係がある。
#問題4:心理学テストと統計テスト1の相関を強くするにはどの指導法を除外すればいいか
指導法データAなし<-指導法データ[指導法データ$指導法!="A",]
指導法データBなし<-指導法データ[指導法データ$指導法!="B",]
指導法データCなし<-指導法データ[指導法データ$指導法!="C",]
指導法データDなし<-指導法データ[指導法データ$指導法!="D",]
cor(指導法データAなし$心理学テスト,指導法データAなし$統計テスト1)
cor(指導法データBなし$心理学テスト,指導法データBなし$統計テスト1)
cor(指導法データCなし$心理学テスト,指導法データCなし$統計テスト1)
cor(指導法データDなし$心理学テスト,指導法データDなし$統計テスト1)
#指導法Dを除外した時に相関が最も強くなる。